class Solution
{
    public:
    //Function to swap odd and even bits.
    unsigned int swapBits(unsigned int n)
    {
        int a=n&(0xAAAAAAAA);
        int b=n&(0x55555555);
        a=a>>1;
        b=b<<1;
        return a|b;
    }
};

